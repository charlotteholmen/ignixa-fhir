    private string GetSafeMethodName(string typeName)
    {
        // Replace special characters to make valid C# method names
        return typeName.Replace("-", "_").Replace(".", "_").Replace("#", "Sharp");
    }

    private string EscapeString(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    private string CapitalizeFirstLetter(string value)
    {
        if (string.IsNullOrEmpty(value))
            return value;

        if (char.IsUpper(value[0]))
            return value; // Already capitalized

        return char.ToUpperInvariant(value[0]) + value.Substring(1);
    }

    private string GenerateReferenceMetadata(string fhirVersion, DefinitionCollection definitions, CSharpStructureProviderConfig config)
    {
        var sb = new StringBuilder();

        // File header
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// <copyright file=\"" + $"{fhirVersion}ReferenceMetadata.g.cs" + "\" company=\"Microsoft Corporation\">");
        sb.AppendLine("//     Copyright (c) Microsoft Corporation. All rights reserved.");
        sb.AppendLine("//     Licensed under the MIT License (MIT). See LICENSE in the repo root for license information.");
        sb.AppendLine("// </copyright>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using Ignixa.SourceNodeSerialization.Models;");
        sb.AppendLine();
        sb.AppendLine($"namespace {config.Namespace};");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine($"/// Pre-generated reference metadata for FHIR {fhirVersion}.");
        sb.AppendLine("/// Provides efficient lookup of which elements are references and their target types.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine($"public static class {fhirVersion}ReferenceMetadata");
        sb.AppendLine("{");

        // Generate metadata dictionary
        sb.AppendLine("    private static readonly Dictionary<string, List<ReferenceFieldMetadata>> _metadata = new()");
        sb.AppendLine("    {");

        // Process all resources
        var resources = definitions.ResourcesByName.OrderBy(kvp => kvp.Key).ToList();

        foreach (var (resourceName, sd) in resources)
        {
            var referenceElements = sd.cgElements(topLevelOnly: true, includeRoot: false, skipSlices: true)
                .Where(e => e.Type.Any(t => t.Code == "Reference"))
                .ToList();

            if (referenceElements.Count == 0)
                continue;

            sb.AppendLine($"        [\"{resourceName}\"] = new List<ReferenceFieldMetadata>");
            sb.AppendLine($"        {{");

            foreach (var element in referenceElements)
            {
                string elementName = element.cgName();
                int min = element.cgCardinalityMin();
                string max = element.Max ?? "*";
                bool inSummary = element.IsSummary ?? false;

                // Extract target profiles
                var referenceType = element.Type.FirstOrDefault(t => t.Code == "Reference");
                var targetProfiles = new List<string>();

                if (referenceType?.TargetProfile != null && referenceType.TargetProfile.Any())
                {
                    foreach (var targetUrl in referenceType.TargetProfile)
                    {
                        // Extract resource name from URL like "http://hl7.org/fhir/StructureDefinition/Patient"
                        string resourceType = targetUrl.Split('/').Last();
                        targetProfiles.Add(resourceType);
                    }
                }

                // Generate field metadata (positional parameters for record)
                string targetTypesArray;
                if (targetProfiles.Count > 0)
                {
                    targetTypesArray = $"new[] {{ {string.Join(", ", targetProfiles.Select(t => $"\"{t}\""))} }}";
                }
                else
                {
                    targetTypesArray = "System.Array.Empty<string>()";
                }

                sb.AppendLine($"            new ReferenceFieldMetadata(\"{elementName}\", {min}, \"{max}\", {targetTypesArray}, {(inSummary ? "true" : "false")}),");
            }

            sb.AppendLine($"        }},");
        }

        sb.AppendLine("    };");
        sb.AppendLine();

        // Generate lookup method
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Gets reference metadata for a resource type.");
        sb.AppendLine("    /// Returns empty list if resource type has no references or is unknown.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [System.CLSCompliant(false)]");
        sb.AppendLine("    public static IReadOnlyList<ReferenceFieldMetadata> GetMetadata(string resourceType)");
        sb.AppendLine("    {");
        sb.AppendLine("        return _metadata.TryGetValue(resourceType, out var metadata)");
        sb.AppendLine("            ? metadata");
        sb.AppendLine("            : System.Array.Empty<ReferenceFieldMetadata>();");
        sb.AppendLine("    }");
        sb.AppendLine();

        // Generate HasReferences method
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Checks if a resource type has any reference elements.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static bool HasReferences(string resourceType)");
        sb.AppendLine("    {");
        sb.AppendLine("        return _metadata.ContainsKey(resourceType);");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private Dictionary<string, (ElementDefinition.ConstraintComponent Constraint, int Count)>? _constraintData;

    private void CollectCommonConstraints(DefinitionCollection definitions)
    {
        // Collect all constraints across all types and count their frequency
        _constraintData = new Dictionary<string, (ElementDefinition.ConstraintComponent Constraint, int Count)>();

        foreach (var sd in definitions.ResourcesByName.Values
            .Concat(definitions.ComplexTypesByName.Values)
            .Concat(definitions.PrimitiveTypesByName.Values))
        {
            if (sd.Snapshot?.Element == null)
            {
                continue;
            }

            foreach (var element in sd.Snapshot.Element)
            {
                if (element.Constraint == null || !element.Constraint.Any())
                {
                    continue;
                }

                foreach (var constraint in element.Constraint)
                {
                    string key = constraint.Key ?? "";
                    if (string.IsNullOrEmpty(key))
                    {
                        continue;
                    }

                    if (!_constraintData.ContainsKey(key))
                    {
                        _constraintData[key] = (constraint, 1);
                    }
                    else
                    {
                        _constraintData[key] = (constraint, _constraintData[key].Count + 1);
                    }
                }
            }
        }

        // Only consider constraints that appear >100 times as "common"
        _commonConstraintKeys = new HashSet<string>(
            _constraintData
                .Where(kvp => kvp.Value.Count > 100)
                .Select(kvp => kvp.Key));
    }

    private void GenerateCommonConstraintsDict(StringBuilder sb)
    {
        if (_constraintData == null || _commonConstraintKeys == null || _commonConstraintKeys.Count == 0)
        {
            return;
        }

        var commonConstraints = _constraintData
            .Where(kvp => _commonConstraintKeys.Contains(kvp.Key))
            .OrderByDescending(kvp => kvp.Value.Count)
            .ToList();

        sb.AppendLine("    // Common constraints dictionary (frequency-optimized)");
        sb.AppendLine("    // These constraints appear >100 times and are shared to reduce code duplication");
        sb.AppendLine("    private static readonly Dictionary<string, ConstraintDefinition> _commonConstraints = new()");
        sb.AppendLine("    {");

        foreach (var (key, (constraint, count)) in commonConstraints)
        {
            string severityValue = constraint.Severity?.ToString() ?? "error";
            string severity = severityValue.Equals("error", StringComparison.OrdinalIgnoreCase)
                ? "ConstraintSeverity.Error"
                : "ConstraintSeverity.Warning";
            string human = EscapeString(constraint.Human ?? "");
            string expression = EscapeString(constraint.Expression ?? "");
            string xpath = string.IsNullOrEmpty(constraint.Xpath) ? "null" : $"\"{EscapeString(constraint.Xpath)}\"";

            sb.AppendLine($"        [\"{EscapeString(key)}\"] = new ConstraintDefinition");
            sb.AppendLine("        {");
            sb.AppendLine($"            Key = \"{EscapeString(key)}\",");
            sb.AppendLine($"            Severity = {severity},");
            sb.AppendLine($"            Human = \"{human}\",");
            sb.AppendLine($"            Expression = \"{expression}\",");
            sb.AppendLine($"            Xpath = {xpath},");
            sb.AppendLine("            AppliesTo = Array.Empty<string>()");
            sb.AppendLine($"        }}, // Used {count} times");
        }

        sb.AppendLine("    };");
        sb.AppendLine();
        sb.AppendLine("    // Helper method to create constraint with specific AppliesTo");
        sb.AppendLine("    private static ConstraintDefinition C(string key, string resourceType) =>");
        sb.AppendLine("        _commonConstraints[key] with { AppliesTo = new[] { resourceType } };");
        sb.AppendLine();
    }
