        sb.AppendLine();
        sb.AppendLine("        public IReadOnlyCollection<IElementDefinitionSummary> GetElements()");
        sb.AppendLine("        {");
        sb.AppendLine("            return _elements ??= _elementsFactory();");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();

