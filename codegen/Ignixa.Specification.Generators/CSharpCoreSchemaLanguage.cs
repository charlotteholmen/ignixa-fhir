/// <summary>
/// Tracks string usage and generates constants for frequently-used strings.
/// </summary>
internal sealed class StringConstantTracker
{
    private const int MinUsageThreshold = 3;
    private readonly Dictionary<string, int> _usageCounts = new();
    private readonly Dictionary<string, string> _stringToConstantName = new();
    private readonly HashSet<string> _usedConstantNames = new();

    /// <summary>Tracks a string usage.</summary>
    public void Track(string value)
    {
        if (string.IsNullOrEmpty(value))
            return;

        _usageCounts[value] = _usageCounts.GetValueOrDefault(value) + 1;
    }

    /// <summary>Finalizes tracking and assigns constant names to frequently-used strings.</summary>
    public void Complete()
    {
        foreach (var kvp in _usageCounts.Where(x => x.Value >= MinUsageThreshold).OrderByDescending(x => x.Value))
        {
            string constantName = GenerateConstantName(kvp.Key);
            _stringToConstantName[kvp.Key] = constantName;
        }
    }

    /// <summary>Gets the constant reference for a string, or null if not tracked.</summary>
    public string? GetConstantReference(string value)
    {
        return _stringToConstantName.TryGetValue(value, out var constantName) ? $"Constants.{constantName}" : null;
    }

    /// <summary>Generates the Constants class code.</summary>
    public void GenerateConstantsClass(StringBuilder sb)
    {
        if (_stringToConstantName.Count == 0)
            return;

        sb.AppendLine("    private static class Constants");
        sb.AppendLine("    {");

        // Group by category
        var constraintKeys = _stringToConstantName.Where(kvp => kvp.Value.StartsWith("Key_")).OrderBy(kvp => kvp.Value);
        var expressions = _stringToConstantName.Where(kvp => kvp.Value.StartsWith("Expr_")).OrderBy(kvp => kvp.Value);
        var humanTexts = _stringToConstantName.Where(kvp => kvp.Value.StartsWith("Human_")).OrderBy(kvp => kvp.Value);
        var valueSets = _stringToConstantName.Where(kvp => kvp.Value.StartsWith("VS_")).OrderBy(kvp => kvp.Value);
        var others = _stringToConstantName.Where(kvp => !kvp.Value.StartsWith("Key_") && !kvp.Value.StartsWith("Expr_") && !kvp.Value.StartsWith("Human_") && !kvp.Value.StartsWith("VS_")).OrderBy(kvp => kvp.Value);

        if (constraintKeys.Any())
        {
            sb.AppendLine("        // Constraint keys");
            foreach (var kvp in constraintKeys)
            {
                string escaped = EscapeStringStatic(kvp.Key);
                sb.AppendLine($"        public const string {kvp.Value} = \"{escaped}\";");
            }
            sb.AppendLine();
        }

        if (expressions.Any())
        {
            sb.AppendLine("        // Constraint expressions");
            foreach (var kvp in expressions)
            {
                string escaped = EscapeStringStatic(kvp.Key);
                sb.AppendLine($"        public const string {kvp.Value} = \"{escaped}\";");
            }
            sb.AppendLine();
        }

        if (humanTexts.Any())
        {
            sb.AppendLine("        // Human-readable constraint descriptions");
            foreach (var kvp in humanTexts)
            {
                string escaped = EscapeStringStatic(kvp.Key);
                sb.AppendLine($"        public const string {kvp.Value} = \"{escaped}\";");
            }
            sb.AppendLine();
        }

        if (valueSets.Any())
        {
            sb.AppendLine("        // ValueSet URLs");
            foreach (var kvp in valueSets)
            {
                string escaped = EscapeStringStatic(kvp.Key);
                sb.AppendLine($"        public const string {kvp.Value} = \"{escaped}\";");
            }
            sb.AppendLine();
        }

        if (others.Any())
        {
            sb.AppendLine("        // Other common strings");
            foreach (var kvp in others)
            {
                string escaped = EscapeStringStatic(kvp.Key);
                sb.AppendLine($"        public const string {kvp.Value} = \"{escaped}\";");
            }
        }

        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private string GenerateConstantName(string value)
    {
        string prefix = DeterminePrefix(value);
        string baseName = SanitizeForIdentifier(value);

        // Truncate to first 40 chars
        if (baseName.Length > 40)
            baseName = baseName.Substring(0, 40);

        string candidateName = prefix + baseName;

        // Handle collisions
        if (_usedConstantNames.Contains(candidateName))
        {
            int counter = 2;
            while (_usedConstantNames.Contains($"{candidateName}{counter}"))
                counter++;
            candidateName = $"{candidateName}{counter}";
        }

        _usedConstantNames.Add(candidateName);
        return candidateName;
    }

    private static string DeterminePrefix(string value)
    {
        // Check if it's a constraint key (short, lowercase with hyphens like "ele-1", "dom-2")
        if (value.Length < 10 && value.Contains("-") && value.Length > 0 && char.IsLower(value[0]))
        {
            return "Key_";
        }

        // Check if it's a ValueSet URL
        if (value.StartsWith("http://hl7.org/fhir/ValueSet/", StringComparison.Ordinal) ||
            value.StartsWith("http://terminology.hl7.org/ValueSet/", StringComparison.Ordinal))
        {
            return "VS_";
        }

        // Check if it looks like a FHIRPath expression (contains operators or function calls)
        if (value.Contains("(") || value.Contains(".") || value.Contains("=") || value.Contains("or ") || value.Contains("and "))
        {
            return "Expr_";
        }

        // Check if it's human-readable text (starts with capital letter and contains spaces)
        if (value.Length > 0 && char.IsUpper(value[0]) && value.Contains(" "))
        {
            return "Human_";
        }

        return "Str_";
    }

    private static string SanitizeForIdentifier(string value)
    {
        // For ValueSet URLs, extract the last segment
        if (value.StartsWith("http://", StringComparison.Ordinal) || value.StartsWith("https://", StringComparison.Ordinal))
        {
            var parts = value.Split('/');
            value = parts[^1];
            // Handle query strings
            if (value.Contains('?'))
                value = value.Substring(0, value.IndexOf('?'));
        }

        // Convert to PascalCase-like identifier
        var sb = new StringBuilder();
        bool capitalizeNext = true;

        foreach (char c in value)
        {
            if (char.IsLetterOrDigit(c))
            {
                sb.Append(capitalizeNext ? char.ToUpperInvariant(c) : c);
                capitalizeNext = false;
            }
            else if (c == ' ' || c == '-' || c == '_' || c == '.' || c == ':' || c == '/')
            {
                capitalizeNext = true;
            }
        }

        string result = sb.ToString();

        // Ensure it doesn't start with a digit
        if (result.Length > 0 && char.IsDigit(result[0]))
            result = "N" + result;

        // Ensure it's not empty
        if (string.IsNullOrEmpty(result))
            result = "Empty";

        return result;
    }

    private static string EscapeStringStatic(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }
}
