/// <summary>
/// Generates C# ISchema implementations using Ignixa.Abstractions interfaces.
/// This is the next-generation provider using IType and ITypeExtended with modern interfaces.
/// </summary>
public sealed class CSharpCoreSchemaLanguage : ILanguage
{
    private const string LanguageName = "CSharpCoreSchema";
    private StringConstantTracker? _constantTracker;

    /// <summary>Gets the language name.</summary>
    public string Name => LanguageName;

    /// <summary>Gets the configuration type.</summary>
    public Type ConfigType => typeof(CSharpCoreSchemaConfig);

    /// <summary>Gets the FHIR primitive type map (not used for this generator).</summary>
    public Dictionary<string, string> FhirPrimitiveTypeMap => new();

    /// <summary>Gets a value indicating whether this language is idempotent.</summary>
    public bool IsIdempotent => true;

    /// <summary>Exports the Core schema provider code.</summary>
    /// <param name="config">The configuration.</param>
    /// <param name="definitions">The definitions to export.</param>
    public void Export(object config, DefinitionCollection definitions)
    {
        if (config is not CSharpCoreSchemaConfig coreConfig)
        {
            throw new ArgumentException($"Configuration must be of type {nameof(CSharpCoreSchemaConfig)}", nameof(config));
        }

        // Get the FHIR version
        string fhirVersion = definitions.FhirSequence switch
        {
            FhirReleases.FhirSequenceCodes.R4 => "R4",
            FhirReleases.FhirSequenceCodes.R4B => "R4B",
            FhirReleases.FhirSequenceCodes.R5 => "R5",
            FhirReleases.FhirSequenceCodes.R6 => "R6",
            FhirReleases.FhirSequenceCodes.STU3 => "STU3",
            _ => throw new ArgumentException($"Unsupported FHIR version: {definitions.FhirSequence}")
        };

        // Get Core FhirVersion enum value
        string coreFhirVersion = definitions.FhirSequence switch
        {
            FhirReleases.FhirSequenceCodes.R4 => "R4",
            FhirReleases.FhirSequenceCodes.R4B => "R4B",
            FhirReleases.FhirSequenceCodes.R5 => "R5",
            FhirReleases.FhirSequenceCodes.R6 => "R6",
            FhirReleases.FhirSequenceCodes.STU3 => "STU3",
            _ => throw new ArgumentException($"Unsupported FHIR version: {definitions.FhirSequence}")
        };

        // Get full FHIR version string
        string fullVersion = definitions.FhirSequence switch
        {
            FhirReleases.FhirSequenceCodes.R4 => "4.0.1",
            FhirReleases.FhirSequenceCodes.R4B => "4.3.0",
            FhirReleases.FhirSequenceCodes.R5 => "5.0.0",
            FhirReleases.FhirSequenceCodes.R6 => "6.0.0-ballot2",
            FhirReleases.FhirSequenceCodes.STU3 => "3.0.2",
            _ => throw new ArgumentException($"Unsupported FHIR version: {definitions.FhirSequence}")
        };

        // Create output directory
        string outputDir = Path.GetFullPath(coreConfig.OutputDirectory);
        Directory.CreateDirectory(outputDir);

        // Generate the Core schema provider class
        string outputPath = Path.Combine(outputDir, $"{fhirVersion}CoreSchemaProvider.g.cs");
        string code = GenerateCoreProviderCode(fhirVersion, coreFhirVersion, fullVersion, definitions, coreConfig);

        File.WriteAllText(outputPath, code, Encoding.UTF8);

        Console.WriteLine($"Generated {outputPath}");
    }

    private string GenerateCoreProviderCode(string fhirVersion, string coreFhirVersion, string fullVersion, DefinitionCollection definitions, CSharpCoreSchemaConfig config)
    {
        // Initialize the constant tracker and perform first pass to collect strings
        _constantTracker = new StringConstantTracker();
        CollectStringsForTracking(definitions);
        _constantTracker.Complete();

        var sb = new StringBuilder();

        // File header
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// <copyright file=\"" + $"{fhirVersion}CoreSchemaProvider.g.cs" + "\" company=\"Ignixa Contributors\">");
        sb.AppendLine("//     Copyright (c) Ignixa Contributors. All rights reserved.");
        sb.AppendLine("//     Licensed under the MIT License (MIT). See LICENSE in the repo root for license information.");
        sb.AppendLine("// </copyright>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Linq;");
        sb.AppendLine("using Ignixa.Abstractions;");
        sb.AppendLine("using Ignixa.Serialization;");
        sb.AppendLine();
        sb.AppendLine($"namespace {config.Namespace};");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine($"/// Pre-generated IFhirSchemaProvider for FHIR {fhirVersion}.");
        sb.AppendLine("/// This provider uses IType and ITypeExtended interfaces for type metadata.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine($"public sealed partial class {fhirVersion}CoreSchemaProvider : Ignixa.Specification.IFhirSchemaProvider");
        sb.AppendLine("{");

        // ISchema.Version property (explicit interface implementation)
        sb.AppendLine($"    Ignixa.Abstractions.FhirVersion Ignixa.Abstractions.ISchema.Version => Ignixa.Abstractions.FhirVersion.{coreFhirVersion};");
        sb.AppendLine();

        // IFhirSchemaProvider.Version property (using FhirSpecification)
        // Note: FhirSpecification enum uses "Stu3" not "STU3"
        string fhirSpecValue = coreFhirVersion == "STU3" ? "Stu3" : coreFhirVersion;
        sb.AppendLine($"    public FhirSpecification Version => FhirSpecification.{fhirSpecValue};");
        sb.AppendLine();

        // FullVersion property
        sb.AppendLine($"    public string FullVersion => \"{fullVersion}\";");
        sb.AppendLine();

        // ResourceTypeNames property - collect resource type names
        var resourceTypeNames = definitions.ResourcesByName.Keys.OrderBy(x => x).ToList();
        sb.AppendLine($"    public IReadOnlySet<string> ResourceTypeNames => _resourceTypeNames;");
        sb.AppendLine();
        sb.AppendLine($"    private static readonly HashSet<string> _resourceTypeNames = new(StringComparer.OrdinalIgnoreCase)");
        sb.AppendLine("    {");
        foreach (var name in resourceTypeNames)
        {
            sb.AppendLine($"        \"{name}\",");
        }
        sb.AppendLine("    };");
        sb.AppendLine();

        // GetTypeDefinition method
        sb.AppendLine("    public Ignixa.Abstractions.IType? GetTypeDefinition(string typeName)");
        sb.AppendLine("    {");
        sb.AppendLine("        return _types.TryGetValue(typeName, out var type) ? type : null;");
        sb.AppendLine("    }");
        sb.AppendLine();

        // IsKnownType method
        sb.AppendLine("    public bool IsKnownType(string typeName)");
        sb.AppendLine("    {");
        sb.AppendLine("        return _types.ContainsKey(typeName);");
        sb.AppendLine("    }");
        sb.AppendLine();

        // Generate Constants class
        _constantTracker.GenerateConstantsClass(sb);

        // Generate types dictionary
        GenerateCoreTypesDictionary(sb, fhirVersion, definitions);

        // Generate nested CoreType class
        GenerateCoreTypeClass(sb);

        sb.AppendLine("}");

        return sb.ToString();
    }

    private void CollectStringsForTracking(DefinitionCollection definitions)
    {
        // Collect all types (primitives, complex types, resources, and BackboneElements)
        var allTypes = new List<(string Name, StructureDefinition SD, bool IsResource, string? BackbonePath)>();

        foreach (var kvp in definitions.PrimitiveTypesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, false, null));
        }

        foreach (var kvp in definitions.ComplexTypesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, false, null));
        }

        foreach (var kvp in definitions.ResourcesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, true, null));

            // Extract BackboneElement types
            var backboneElements = ExtractBackboneElements(kvp.Value);
            foreach (var (backboneName, backbonePath) in backboneElements)
            {
                allTypes.Add((backboneName, kvp.Value, false, backbonePath));
            }
        }

        // Scan all elements to track string usage
        foreach (var (name, sd, _, backbonePath) in allTypes)
        {
            var elements = backbonePath != null
                ? sd.cgElements(forBackbonePath: backbonePath, topLevelOnly: true, includeRoot: false, skipSlices: true).ToList()
                : sd.cgElements(topLevelOnly: true, includeRoot: false, skipSlices: true).ToList();

            foreach (var element in elements)
            {
                TrackElementStrings(element);
            }
        }
    }

    private void TrackElementStrings(ElementDefinition element)
    {
        if (_constantTracker == null)
            return;

        // Track constraint keys, expressions, and human text
        if (element.Constraint != null)
        {
            foreach (var constraint in element.Constraint)
            {
                if (!string.IsNullOrEmpty(constraint.Key))
                    _constantTracker.Track(constraint.Key);
                if (!string.IsNullOrEmpty(constraint.Expression))
                    _constantTracker.Track(constraint.Expression);
                if (!string.IsNullOrEmpty(constraint.Human))
                    _constantTracker.Track(constraint.Human);
            }
        }

        // Track binding ValueSet URLs
        if (element.Binding != null && !string.IsNullOrEmpty(element.Binding.ValueSet))
        {
            _constantTracker.Track(element.Binding.ValueSet);
        }

        // Track type names
        foreach (var type in element.Type)
        {
            if (!string.IsNullOrEmpty(type.Code))
            {
                string normalized = NormalizeFhirPathTypeUrl(type.Code);
                _constantTracker.Track(normalized);
            }
        }
    }

    private void GenerateCoreTypesDictionary(StringBuilder sb, string fhirVersion, DefinitionCollection definitions)
    {
        sb.AppendLine("    private static readonly Dictionary<string, Ignixa.Abstractions.IType> _types = new(StringComparer.OrdinalIgnoreCase)");
        sb.AppendLine("    {");

        // Collect all types (primitives, complex types, resources, and BackboneElements)
        var allTypes = new List<(string Name, StructureDefinition SD, bool IsResource, string? BackbonePath)>();

        foreach (var kvp in definitions.PrimitiveTypesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, false, null));
        }

        foreach (var kvp in definitions.ComplexTypesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, false, null));
        }

        foreach (var kvp in definitions.ResourcesByName)
        {
            allTypes.Add((kvp.Key, kvp.Value, true, null));

            // Extract BackboneElement types
            var backboneElements = ExtractBackboneElements(kvp.Value);
            foreach (var (backboneName, backbonePath) in backboneElements)
            {
                allTypes.Add((backboneName, kvp.Value, false, backbonePath));
            }
        }

        // Sort alphabetically
        allTypes = allTypes.OrderBy(t => t.Name).ToList();

        foreach (var (name, sd, isResource, backbonePath) in allTypes)
        {
            bool isAbstract = sd.Abstract ?? false;
            string primitive = MapToPrimitive(name);

            sb.AppendLine($"        [\"{name}\"] = new CoreType(");
            sb.AppendLine($"            name: \"{name}\",");
            sb.AppendLine($"            primitive: Ignixa.Abstractions.FhirPrimitive.{primitive},");
            sb.AppendLine($"            isResource: {(isResource ? "true" : "false")},");
            sb.AppendLine($"            isAbstract: {(isAbstract ? "true" : "false")},");
            sb.AppendLine($"            isChoiceElement: false,"); // Top-level types are not choice elements
            sb.AppendLine($"            isModifier: false,"); // Will be set per-element in children
            sb.AppendLine($"            isCollection: false,");
            sb.AppendLine($"            isRequired: false,");
            sb.AppendLine($"            inSummary: false,");
            sb.AppendLine($"            order: 0,");
            sb.AppendLine($"            childrenFactory: () => {GetSafeMethodName(name)}_Children(),");
            sb.AppendLine($"            min: 0,");
            sb.AppendLine($"            max: \"1\",");
            sb.AppendLine($"            constraints: null,");
            sb.AppendLine($"            binding: null,");
            sb.AppendLine($"            fixedValue: null,");
            sb.AppendLine($"            patternValue: null,");
            sb.AppendLine($"            types: new[] {{ new Ignixa.Abstractions.TypeReferenceDefinition(\"{name}\") }},");
            sb.AppendLine($"            defaultTypeName: \"{name}\",");
            sb.AppendLine($"            referenceTargets: null,");
            sb.AppendLine($"            contentReference: null");
            sb.AppendLine($"        ),");
        }

        sb.AppendLine("    };");
        sb.AppendLine();

        // Generate child factory methods for each type
        foreach (var (name, sd, _, backbonePath) in allTypes)
        {
            GenerateChildrenMethod(sb, name, sd, definitions, backbonePath);
        }
    }

    private void GenerateChildrenMethod(StringBuilder sb, string typeName, StructureDefinition sd, DefinitionCollection definitions, string? backbonePath = null)
    {
        string safeName = GetSafeMethodName(typeName);

        sb.AppendLine($"    private static IReadOnlyList<Ignixa.Abstractions.IType> {safeName}_Children()");
        sb.AppendLine("    {");
        sb.AppendLine("        return new Ignixa.Abstractions.IType[]");
        sb.AppendLine("        {");

        // Get elements for this type
        var elements = backbonePath != null
            ? sd.cgElements(forBackbonePath: backbonePath, topLevelOnly: true, includeRoot: false, skipSlices: true).ToList()
            : sd.cgElements(topLevelOnly: true, includeRoot: false, skipSlices: true).ToList();

        int order = 0;
        foreach (var element in elements)
        {
            GenerateCoreTypeChild(sb, element, definitions, order);
            order++;
        }

        sb.AppendLine("        };");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private void GenerateCoreTypeChild(StringBuilder sb, ElementDefinition element, DefinitionCollection definitions, int order)
    {
        string elementName = element.cgName();
        bool isCollection = element.cgIsArray();
        bool isRequired = element.cgCardinalityMin() > 0;
        bool inSummary = element.IsSummary ?? false;
        bool isChoiceElement = element.Path.EndsWith("[x]", StringComparison.Ordinal);
        bool isModifier = element.IsModifier ?? false;

        // Strip [x] suffix from choice element names
        if (isChoiceElement && elementName.EndsWith("[x]", StringComparison.Ordinal))
        {
            elementName = elementName.Substring(0, elementName.Length - 3);
        }

        // Determine if this element is a resource type
        bool isResource = element.Type.Any(t => definitions.ResourcesByName.ContainsKey(t.Code ?? string.Empty));

        // Get type names
        var typeNames = element.Type.Select(t => NormalizeFhirPathTypeUrl(t.Code ?? "Element")).ToList();
        string typeName = typeNames.FirstOrDefault() ?? "Element";
        string primitive = MapToPrimitive(typeName);

        // Extract extended metadata
        var (min, max, constraints, binding, fixedValue, patternValue, typeArray, defaultTypeName, referenceTargets, contentReference, slicing) =
            ExtractExtendedMetadata(element, definitions, typeName);

        sb.AppendLine($"            new CoreType(");
        sb.AppendLine($"                name: \"{EscapeString(elementName)}\",");
        sb.AppendLine($"                primitive: Ignixa.Abstractions.FhirPrimitive.{primitive},");
        sb.AppendLine($"                isResource: {(isResource ? "true" : "false")},");
        sb.AppendLine($"                isAbstract: false,");
        sb.AppendLine($"                isChoiceElement: {(isChoiceElement ? "true" : "false")},");
        sb.AppendLine($"                isModifier: {(isModifier ? "true" : "false")},");
        sb.AppendLine($"                isCollection: {(isCollection ? "true" : "false")},");
        sb.AppendLine($"                isRequired: {(isRequired ? "true" : "false")},");
        sb.AppendLine($"                inSummary: {(inSummary ? "true" : "false")},");
        sb.AppendLine($"                order: {order},");
        sb.AppendLine($"                childrenFactory: null,"); // Children are resolved via type lookup
        sb.AppendLine($"                min: {min},");
        sb.AppendLine($"                max: {max},");
        sb.AppendLine($"                constraints: {constraints},");
        sb.AppendLine($"                binding: {binding},");
        sb.AppendLine($"                fixedValue: {fixedValue},");
        sb.AppendLine($"                patternValue: {patternValue},");
        sb.AppendLine($"                types: {typeArray},");
        sb.AppendLine($"                defaultTypeName: {defaultTypeName},");
        sb.AppendLine($"                referenceTargets: {referenceTargets},");
        sb.AppendLine($"                contentReference: {contentReference},");
        sb.AppendLine($"                slicing: {slicing}");
        sb.AppendLine($"            ),");
    }

    private (string min, string max, string constraints, string binding, string fixedValue, string patternValue, string typeArray, string defaultTypeName, string referenceTargets, string contentReference, string slicing)
        ExtractExtendedMetadata(ElementDefinition element, DefinitionCollection definitions, string elementTypeName)
    {
        // 1. Min/Max cardinality
        string min = element.Min.HasValue ? element.Min.Value.ToString() : "null";
        string max = !string.IsNullOrEmpty(element.Max) ? $"\"{EscapeString(element.Max)}\"" : "null";

        // 2. Constraints
        string constraints = "null";
        if (element.Constraint != null && element.Constraint.Any())
        {
            // Track the AppliesTo element type name for constant generation
            _constantTracker?.Track(elementTypeName);

            var constraintList = element.Constraint
                .Select(c =>
                {
                    // Use constant reference for constraint key if available
                    string keyValue = c.Key ?? "";
                    string key = _constantTracker?.GetConstantReference(keyValue) ?? $"\"{EscapeString(keyValue)}\"";

                    string severityValue = c.Severity?.ToString()?.ToLowerInvariant() ?? "error";
                    string severity = $"\"{EscapeString(severityValue)}\"";

                    // Use constants for human and expression if available
                    string humanValue = c.Human ?? "";
                    string human = _constantTracker?.GetConstantReference(humanValue) ?? $"\"{EscapeString(humanValue)}\"";

                    string expressionValue = c.Expression ?? "";
                    string expression = _constantTracker?.GetConstantReference(expressionValue) ?? $"\"{EscapeString(expressionValue)}\"";

                    string xpath = c.Xpath != null ? $"\"{EscapeString(c.Xpath)}\"" : "null";

                    return $"new Ignixa.Abstractions.ConstraintDefinition {{ Key = {key}, Severity = {severity}, Human = {human}, Expression = {expression}, Xpath = {xpath} }}";
                })
                .ToList();

            if (constraintList.Count > 0)
            {
                constraints = $"new[] {{ {string.Join(", ", constraintList)} }}";
            }
        }

        // 3. Binding
        string binding = "null";
        if (element.Binding != null && !string.IsNullOrEmpty(element.Binding.ValueSet))
        {
            string strength = element.Binding.Strength?.ToString() ?? "Unknown";
            string valueSetUrl = _constantTracker?.GetConstantReference(element.Binding.ValueSet) ?? $"\"{EscapeString(element.Binding.ValueSet)}\"";
            binding = $"new Ignixa.Abstractions.BindingMetadata({valueSetUrl}, \"{strength}\")";
        }

        // 4. Fixed Value (serialize as JSON)
        string fixedValue = "null";
        if (element.Fixed != null)
        {
            string json = SerializeToJson(element.Fixed);
            if (!string.IsNullOrEmpty(json))
            {
                fixedValue = $"\"{EscapeString(json)}\"";
            }
        }

        // 5. Pattern Value (serialize as JSON)
        string patternValue = "null";
        if (element.Pattern != null)
        {
            string json = SerializeToJson(element.Pattern);
            if (!string.IsNullOrEmpty(json))
            {
                patternValue = $"\"{EscapeString(json)}\"";
            }
        }

        // 6. Type array (normalize FHIRPath URLs and create TypeReference objects)
        string typeArray = "null";
        if (element.Type != null && element.Type.Any())
        {
            var typeRefs = element.Type.Select(t =>
            {
                string code = NormalizeFhirPathTypeUrl(t.Code ?? "Element");
                string codeRef = _constantTracker?.GetConstantReference(code) ?? $"\"{EscapeString(code)}\"";

                string profile = "null";
                if (!string.IsNullOrEmpty(t.Profile?.FirstOrDefault()))
                {
                    profile = $"\"{EscapeString(t.Profile.First())}\"";
                }

                string targetProfile = "null";
                if (!string.IsNullOrEmpty(t.TargetProfile?.FirstOrDefault()))
                {
                    targetProfile = $"\"{EscapeString(t.TargetProfile.First())}\"";
                }

                string aggregation = "null";
                if (t.Aggregation != null && t.Aggregation.Any())
                {
                    var aggValues = t.Aggregation.Select(a => $"\"{EscapeString(a.ToString() ?? "")}\"");
                    aggregation = $"new[] {{ {string.Join(", ", aggValues)} }}";
                }

                string versioning = "null";
                if (t.Versioning.HasValue)
                {
                    versioning = $"\"{EscapeString(t.Versioning.Value.ToString() ?? "")}\"";
                }

                return $"new Ignixa.Abstractions.TypeReferenceDefinition({codeRef}, {profile}, {targetProfile}, {aggregation}, {versioning})";
            });
            typeArray = $"new[] {{ {string.Join(", ", typeRefs)} }}";
        }

        // 7. Default type name
        string defaultTypeName = "null";
        var firstTypeCode = element.Type?.FirstOrDefault()?.Code;
        if (firstTypeCode != null)
        {
            string firstType = NormalizeFhirPathTypeUrl(firstTypeCode);
            defaultTypeName = _constantTracker?.GetConstantReference(firstType) ?? $"\"{EscapeString(firstType)}\"";
        }

        // 8. Reference Targets
        string referenceTargets = "null";
        var referenceType = element.Type?.FirstOrDefault(t => t.Code == "Reference");
        if (referenceType?.TargetProfile != null && referenceType.TargetProfile.Any())
        {
            var targets = referenceType.TargetProfile
                .Select(url => url.Split('/').Last())
                .Where(t => !string.IsNullOrEmpty(t))
                .ToList();

            if (targets.Count > 0)
            {
                referenceTargets = $"new[] {{ {string.Join(", ", targets.Select(t => $"\"{EscapeString(t)}\""))} }}";
            }
        }

        // 9. Content Reference
        string contentReference = "null";
        if (!string.IsNullOrEmpty(element.ContentReference))
        {
            contentReference = $"\"{EscapeString(element.ContentReference)}\"";
        }

        // 10. Slicing
        string slicing = "null";
        if (element.Slicing != null)
        {
            var discriminators = element.Slicing.Discriminator?
                .Select(d => $"{d.Type}:{d.Path}")
                .ToArray() ?? Array.Empty<string>();

            string rules = element.Slicing.Rules?.ToString() ?? "Open";
            bool ordered = element.Slicing.Ordered ?? false;

            if (discriminators.Length > 0)
            {
                string discriminatorArray = $"new[] {{ {string.Join(", ", discriminators.Select(d => $"\"{EscapeString(d)}\""))} }}";
                slicing = $"new Ignixa.Abstractions.SlicingMetadata({discriminatorArray}, \"{rules}\", {(ordered ? "true" : "false")})";
            }
        }

        return (min, max, constraints, binding, fixedValue, patternValue, typeArray, defaultTypeName, referenceTargets, contentReference, slicing);
    }

    private string SerializeToJson(DataType value)
    {
        try
        {
            // Use Firely SDK's JSON serializer
            var serializer = new Hl7.Fhir.Serialization.FhirJsonSerializer();
            return serializer.SerializeToString(value);
        }
        catch
        {
            // Fallback to simple string representation
            return value.ToString() ?? string.Empty;
        }
    }

    private void GenerateCoreTypeClass(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Core implementation of ITypeExtended for FHIR type metadata with extended properties.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    private sealed class CoreType : Ignixa.Abstractions.ITypeExtended");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly Func<IReadOnlyList<Ignixa.Abstractions.IType>>? _childrenFactory;");
        sb.AppendLine("        private IReadOnlyList<Ignixa.Abstractions.IType>? _children;");
        sb.AppendLine();
        sb.AppendLine("        public CoreType(");
        sb.AppendLine("            string name,");
        sb.AppendLine("            Ignixa.Abstractions.FhirPrimitive primitive,");
        sb.AppendLine("            bool isResource,");
        sb.AppendLine("            bool isAbstract,");
        sb.AppendLine("            bool isChoiceElement,");
