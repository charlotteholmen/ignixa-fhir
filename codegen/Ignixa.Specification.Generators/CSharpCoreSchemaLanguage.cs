        sb.AppendLine("            IReadOnlyList<string>? referenceTargets = null,");
        sb.AppendLine("            string? contentReference = null,");
        sb.AppendLine("            Ignixa.Abstractions.SlicingMetadata? slicing = null)");
        sb.AppendLine("        {");
